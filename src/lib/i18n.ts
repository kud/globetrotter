const dict: Record<Locale, Dict> = { en, fr, es, de }

export type Translate = (
  key: keyof typeof en,
  params?: Record<string, string | number>,
) => string

const interpolate = (s: string, params?: Record<string, string | number>) =>
  params ? s.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? `{${k}}`)) : s

export const translate =
  (locale: Locale): Translate =>
  (key, params) =>
    interpolate(dict[locale]?.[key] ?? en[key] ?? String(key), params)

export const useT = (): Translate => {
  const locale = useTravelStore((s) => s.locale)
  return translate(locale)
}

// Region-qualified tags so dates render day-first (European) even in English.
const DATE_LOCALE: Record<Locale, string> = {
  en: "en-GB",
  fr: "fr-FR",
  es: "es-ES",
  de: "de-DE",
}

export const dateLocale = (locale: Locale): string => DATE_LOCALE[locale]

export const statusKey = (
  status?: "visited" | "wishlist" | "blocked",
): keyof typeof en =>
  status === "visited"
    ? "status.visited"
    : status === "wishlist"
      ? "status.wishlist"
      : status === "blocked"
        ? "status.blocked"
        : "status.notyet"

export const detectLocale = (): Locale => {
  if (typeof navigator === "undefined") return "en"
  const lang = navigator.language.slice(0, 2).toLowerCase()
  return (LOCALES.some((l) => l.id === lang) ? lang : "en") as Locale
}
